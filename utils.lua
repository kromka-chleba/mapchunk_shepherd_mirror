-- Globals
local ms = mapchunk_shepherd

-- Allows functions to accept both normal tables and multiple unpacked arguments.
function ms.unpack_args(...)
    local args = {...}
    if type(args[1]) == "table" then
        return args[1]
    end
    return args
end
